using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Demo.App.Models
{
    public class Calculator
    {
        public int add(int x, int y)
        {
            return x + y;
        }
        public int sub(int x, int y)
        {
            return x - y;
        }
        public int mul(int x, int y)
        {
            return x * y;
        }
        public int div(int x, int y)
        {
            return x - y;
        }
    }
}